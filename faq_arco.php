				<div class="contenedor-pregunta">
					<p class="pregunta">¿Qué significa Derechos ARCO?<img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">
					<span  class="remarcado">A</span>cceso: Conocer en todo momento quién dispone de tus datos y para qué están siendo utilizados.</br>
					<span  class="remarcado">R</span>ectificación: Solicitar rectificación de tus datos en caso de que resulten incompletos o inexactos..</br>
					<span  class="remarcado">C</span>ancelación: Solicitar la cancelación de los mismos por no ajustarse a las disposiciones aplicables..</br>
					<span  class="remarcado">O</span>posición: Oponerse al uso de tus datos si es que los mismos fueron obtenidos sin tu consentimiento..</br>
El ejercicio de cualquiera de los Derechos ARCO no impide el ejercicio de alguno otro derecho..</br></p>
				</div>
				<div class="contenedor-pregunta">
					<p class="pregunta">¿Que es la Solicitud de Derechos ARCO? <img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">Se refiere a aquel formato a través del cual el Titular de datos personales puede solicitar el  Derecho de acceso, rectificación, cancelación u oposición sobre el tratamiento de sus datos, ante el Sujeto regulado que esté en posesión de los mismos.</p>
				</div>
        <div class="contenedor-pregunta">
					<p class="pregunta">¿Que es un dato personal?<img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">
					Cualquier información concerniente a una persona física identificada o identificable.
					
					</p>
				</div>
        <div class="contenedor-pregunta">
					<p class="pregunta">¿Quien puede ejercer los Derechos ARCO?<img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">En base al Artículo 22 de la Ley Federal de Protección de Datos Personales en Posesión de los Particulares, cualquier Titular, o en su caso su representante legal, podrá ejercer los derechos de acceso, rectificación, cancelación y oposición previstos en la referida Ley. El ejercicio de cualquiera de ellos no es requisito previo ni impide el ejercicio de otro.
Los datos personales deben ser resguardados de tal manera que permitan el ejercicio sin dilación de estos derechos.
</p>
				</div>
      
             <div class="contenedor-pregunta">
					<p class="pregunta">¿Quién es un Titular de acuerdo a la LFPDPPP? <img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">La persona física a quien corresponden los datos personales.</p>
				</div>
        <div class="contenedor-pregunta">
					<p class="pregunta">¿Quién es un Tercero de acuerdo a la LFPDPPP?<img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">La persona física o moral, nacional o extranjera, distinta del Titular o del responsable de los datos.</p>
				</div>
        <div class="contenedor-pregunta">
					<p class="pregunta">¿Quién es un Responsable de acuerdo a la LFPDPPP?<img src="img/suma.svg" alt="Abrir Respuesta" /></p>
					<p class="respuesta">Persona física o moral de carácter privado que decide sobre el tratamiento de datos personales.</p>
